w=readmatrix('MAF_EWSnet_Sensitivity_Data.csv');
A=reshape(w(:,3),36,46);
A=flipud(A);
c_start = [1, 1, 1];           
c_end   = [0.01, 0.75, 0.24];  
N = 256;
r = linspace(c_start(1), c_end(1), N)';
g = linspace(c_start(2), c_end(2), N)';
b = linspace(c_start(3), c_end(3), N)';
customMap = [r g b];
imagesc(A);
colormap(customMap);
clim([0 1]);xlim([1 46]); ylim([1 36]);
colorbar;
set(gca,'Linewidth',1,'Fontweight','bold','tickdir','out','Fontsize',12,'Linewidth',1.5);
ax=gca;ax.TickLength=[0.008, 0.01];ax.XAxis.Color='k';ax.YAxis.Color='k';
xticks([1 10 19 28 37 46]);
xticklabels({'1', '2.62', '4.24', '5.86', '7.48', '9.1'});
yticks([1 6 11 16 21 26 31 36]);
yticklabels({'75', '65', '55', '45', '35', '25', '15', '5'});
xlabel('Time series segment');
ylabel('Bandwidth');
title('MAF');
yline(16,'LineWidth',3,'LineStyle','--','Alpha',1,'Color','r');